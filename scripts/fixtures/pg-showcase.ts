/**
 * Apply the PG exotic-type showcase into schema `showcase`. Best-effort per
 * table: a block that fails (e.g. `xml` on a build without libxml) is logged
 * and skipped so the rest of the gallery still lands.
 */
export async function applyPgShowcase(
  c: Client,
  log: (entity: string, count: number, ms: number) => void,
): Promise<void> {
  const preludeStart = Date.now();
  try {
    await c.query(PRELUDE);
  } catch (err) {
    log(
      `showcase.<prelude failed: ${err instanceof Error ? err.message : String(err)}>`,
      0,
      Date.now() - preludeStart,
    );
    return; // schema/enum missing → every table would fail; bail early.
  }

  for (const block of buildBlocks()) {
    const start = Date.now();
    try {
      // Multi-statement batches resolve to an array of results; the bulk
      // INSERT is last, so its rowCount is the meaningful figure to log.
      const res = await c.query(block.sql);
      const rowCount = Array.isArray(res)
        ? (res[res.length - 1]?.rowCount ?? 0)
        : (res.rowCount ?? 0);
      log(`showcase.${block.name}`, rowCount, Date.now() - start);
    } catch (err) {
      log(
        `showcase.${block.name} <skipped: ${err instanceof Error ? err.message : String(err)}>`,
        0,
        Date.now() - start,
      );
    }
  }
}
