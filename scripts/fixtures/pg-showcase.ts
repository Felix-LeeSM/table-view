// PG-native "exotic" type showcase — the types the portable fixture pipeline
// (base.yaml → 9 engines) can't express uniformly across DBMSs. Lives only in
// Postgres, under schema `showcase`, and is seeded as hand-authored SQL rather
// than through the Faker generator: a valid PostGIS-free `point`, an `int4range`
// or a real image `bytea` is clearer written by hand than probabilistically
// generated.
//
// Each table carries two row groups:
//   1) a handful of curated *edge* rows (all-NULL, empty collections, unbounded
//      ranges, unicode/emoji/RTL, boundary numerics), and
//   2) a `generate_series` bulk fill (default 1000 rows, override with
//      SHOWCASE_ROWS) so the tables have realistic volume for paging/sorting.
//
// Each table is applied as an isolated statement batch: an environment missing
// an optional feature (e.g. libxml for the `xml` column) drops only that one
// table, not the whole gallery.
//
// Idempotent: every block DROPs before CREATE, so it is safe to re-run on each
// `fixtures:load` / `db:reset`.
import { readFileSync } from "node:fs";
import { resolve } from "node:path";
import type { Client } from "pg";

// Tiny 40px JPEG thumbnails (picsum.photos), committed as base64 so seeding
// needs no network. Regenerate with:
//   curl -sL https://picsum.photos/seed/<name>/40.jpg | base64
// Resolved from cwd (repo root) to match spec.ts's FIXTURES_DIR convention;
// the fixture CLI and vitest both run from the repo root.
const BLOBS = JSON.parse(
  readFileSync(
    resolve(process.cwd(), "scripts/fixtures/showcase-blobs.json"),
    "utf8",
  ),
) as Record<string, string>;

// Bulk row count per showcase table. Env override keeps the "pass it in" knob
// consistent with the rest of the fixture CLI.
const BULK_ROWS = Math.max(0, Number(process.env.SHOWCASE_ROWS ?? 1000));

interface ShowcaseBlock {
  name: string;
  sql: string;
}

/**
 * Prelude — schema + optional-contrib extensions + the native enum type.
 * `setseed` makes the random()-driven bulk fill reproducible within a run.
 * Kept separate so its failure is reported distinctly from a table's.
 */
export const PRELUDE = `
CREATE SCHEMA IF NOT EXISTS showcase;
CREATE EXTENSION IF NOT EXISTS hstore;
DO $$ BEGIN
  CREATE TYPE showcase.mood AS ENUM ('happy', 'sad', 'neutral', 'excited');
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;
-- DOMAIN = a constrained type alias (closest PG analogue to a SYNONYM/alias).
DO $$ BEGIN
  CREATE DOMAIN showcase.us_zip AS text CHECK (VALUE ~ '^[0-9]{5}$');
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;
-- Composite type — a user-defined row type.
DO $$ BEGIN
  CREATE TYPE showcase.money_amount AS (amount numeric, currency text);
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;
SELECT setseed(0.42);
`;

